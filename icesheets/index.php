         <h4>Surface elevation change</h4>
         <p>
This data product is suitable for investigating where surface 
elevation change occurs and the rate at which it occurs over the 
Greenland and Antarctic ice sheets. 
The elevation change product can also be used to explore the processes that 
drive changes to the ice sheets. Ice sheet surface elevation change is indicative 
of mass change. </p>
<h4>Mass balance</h4><p>
This data product is suitable for understanding the current state and changes to the Greenland and Antarctic ice sheets. By measuring mass balance at 5 km2 resolution, it will be possible to investigate spatiotemporal variability, where mass changes occur and the rate at which they occur. This information is crucial for estimating the ice sheet contribution to sea level rise.
</p>
